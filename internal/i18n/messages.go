func translate(locale string, key string) string {
  if localeMessages, ok := messages[locale]; ok {
    if message, ok := localeMessages[key]; ok {
      return message
}
  }
  if locale != DefaultLocale {
    if message, ok := messages[DefaultLocale][key]; ok {
      return message
}
  }
  return key
}
